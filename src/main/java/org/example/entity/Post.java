package org.example.entity;

public class Post {
    String PostTitle ;
    String PostContent ;

    public String getPostTitle() {
        return PostTitle;
    }

    public String getPostContent() {
        return PostContent;
    }

    public void setPostTitle(String postTitle) {
        PostTitle = postTitle;
    }

    public void setPostContent(String postContent) {
        PostContent = postContent;
    }
}
